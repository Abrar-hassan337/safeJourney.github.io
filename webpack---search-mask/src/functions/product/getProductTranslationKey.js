export const getProductTranslationKey = (productType, type) => {
    if (productType.indexOf('.') === -1 && productType.startsWith('pl_') && type === 'label') {
        return `passenger.${productType}`;
    }

    if (!translationMapping[productType] || !translationMapping[productType][type]) {
        return productType;
    }

    return translationMapping[productType][type];
};